function u_sf = solidVelocityFcn(u_t, E_lp, Global)
% -------------------------------------------------------------------------

    g     = Global.g;
    D     = Global.reactor.rID_2;
    u_sg  = Global.fDynamics.usg0;
    u_glp = u_sg./E_lp;

% -------------------------------------------------------------------------
    Fr_gf = u_sg./((g.*D).^(0.5));
    Fr_t  = u_t./((g.*D).^(0.5));
    phi   = 1 + (5.6./Fr_gf) + 0.47.*(Fr_t.^(0.41));
% -------------------------------------------------------------------------
    u_sf              = u_glp./phi;
    u_sf(isinf(u_sf)) = 0;
    u_sf(isnan(u_sf)) = 0;
% -------------------------------------------------------------------------
 % u_sf(1:60,1) = 0.4;                 % ===========> valor ficticio
% -------------------------------------------------------------------------
end